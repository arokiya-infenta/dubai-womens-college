<script>
var base_url = "<?php echo base_url(); ?>";
	$(document).ready(function(){
var myTable = $('#exammarks-datatable').DataTable();
var allPages = myTable.rows().nodes().to$();
	$('#stream').change(function(){
			$('#dept').css('display','block');
			$('#department').empty();
		  var stream = $(this).val();	
			if (stream!='') {
            $.ajax({
                url: base_url + "coe/getProgram",
                type: 'POST',
                cache: false,
				//dataType: "html",
                data: {
                    stream: stream
                },
                success: function (data) {
					$('#department').append(data);
                }
            });
        }else{
			$('#dept').css('display','none');
		}
		});
		
	
		$('#cc_submit').click(function(){
		var cc=Array();	
		var cc_not=Array();	
		var main_id=$('#stream').val();	
		var course_id=$('#department').val();	
		var subject_id=$('#subject').val();	
		var batch=$('#batch').val();
			allPages.each(function(){
			$(this).closest('tr').find('input[class=cc]:checked').each(function(){
            cc.push($(this).data('student'));
            });
		});
		allPages.each(function(){
			$(this).closest('tr').find('input[class=cc]:not(:checked)').each(function(){
            cc_not.push($(this).data('student'));
            });
		});
		if (confirm('Are you sure to add the Marks?')) {
            $.ajax({
                url: base_url + "coe/certificateCourseReport",
                type: 'POST',
                cache: false,
				//dataType: "html",
                data: {
                    cc: cc,main_id: main_id,course_id: course_id,cc_not: cc_not,subject_id: subject_id,batch: batch,
                },
                success: function (data) {
                }
            });
					alert('Certificate Course Marks Added Successfully!!');
		}
		});
		
	});
	</script>